public class WordFrequency {

    private String word;
    private int frequency = 0;

    public WordFrequency(String word, int frequency){
        this.word = word;
        this.frequency = frequency;
    }

    public String getWord() {
        return word;
    }

    public int getFrequency() {
        return frequency;
    }


}
